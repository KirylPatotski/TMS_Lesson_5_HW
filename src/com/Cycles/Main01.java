package com.Cycles;
/**Prints every odd integer between 1 and 99.*/
public class Main01 {
    public static void main(String[] args) {
        System.out.println("Start");
        for (int i = 1; i < 100; i++){
            System.out.println(i);
            i++;
        }
        System.out.println("End");
    }
}


