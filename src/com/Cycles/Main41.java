package com.Cycles;
/**Prints all numbers between 0 and -10 times 5.*/
public class Main41 {
    public static void main(String[] args) {
        System.out.println("Start");
        int i = 0;
        while (i > -11){
            System.out.println(i*5);
            i--;
        }
        System.out.println("End");
    }
}


