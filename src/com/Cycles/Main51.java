package com.Cycles;
/**Prints every  perfect square number between 100 and 400.*/
public class Main51 {
    public static void main(String[] args) {
        System.out.println("Start");
        int i = 10;
        while (i != 21){
            System.out.println(i*i);
            i++;
        }
        System.out.println("End");
    }
}


